function love.conf(t)
  t.version = "11.3"
  t.title = "Lights"
  t.identity = "lights"
  t.window.minwidth = 160
  t.window.minheight = 90
  t.window.width = 1280
  t.window.height = 720
  t.window.resizable = true
  t.window.fullscreentype = "desktop" -- "exclusive"
  t.window.fullscreen = false
  t.window.vsync = 1
end
